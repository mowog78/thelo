<p>Thelo is the result of a reflection on the use of typography on digital media. The family is inspired by optical adjustments commonly practiced by punch-cutters at the time of metal typography. Applied to digital typography, the principle of optical sizes can improve reading experiences on screen.</p>

<p>Contrary to commonly accepted practices that tend to maintain identical shapes throughout different body sizes, Thelo’s letterforms adapt according to the scale in which they are used to suit different reading contexts on the web.</p>

<p>The entire family is based on a grid that simulates pixels and sub-pixels. This structure was the starting point for the design of three specific sizes, 8, 16 and 32 pixels, that correspond respectively to Micro, Text and Grand. The use of a constant UPM (units per em) value allows for a consistent relationship between the three variants. This rigorous drawing method takes into consideration digital display constraints and provides a sharp rendering of characters on screen.</p>

<p>Thelo was named after the Mexican cactus variety Thelocactus, a parallel between the roughness of screen displays and the drylands where the cactus grow.</p>

